import { getDate } from "../getDate";
import { test } from "@jest/globals";

test("Проверка работы с валидными данными", () => {
  expect(
    getDate(6, [
      "2024-09-06T18:00",
      "2024-09-07T18:00",
      "2024-09-08T18:00",
      "2024-09-09T18:00",
      "2024-09-10T18:00",
      "2024-09-11T18:00",
      "2024-09-12T18:00",
    ])
  ).toBe("ЧТ 12");
  expect(
    getDate(3, [
      "2024-09-16T18:00",
      "2024-09-17T18:00",
      "2024-09-18T18:00",
      "2024-09-19T18:00",
      "2024-09-20T18:00",
      "2024-09-21T18:00",
      "2024-09-22T18:00",
    ])
  ).toBe("ЧТ 19");
  expect(
    getDate(0, [
      "2024-09-16T18:00",
      "2024-09-17T18:00",
      "2024-09-18T18:00",
      "2024-09-19T18:00",
      "2024-09-20T18:00",
      "2024-09-21T18:00",
      "2024-09-22T18:00",
    ])
  ).toBe("ПН 16");
});
